public class FeedBackDetails {


    private String mId;
    private String mQuestion;
    private String mAnswerFormat;
    private String mAnswerFormatName;

    public String getmId() {
        return mId;
    }

    public void setmId(String mId) {
        this.mId = mId;
    }

    public String getmQuestion() {
        return mQuestion;
    }

    public void setmQuestion(String mQuestion) {
        this.mQuestion = mQuestion;
    }

    public String getmAnswerFormat() {
        return mAnswerFormat;
    }

    public void setmAnswerFormat(String mAnswerFormat) {
        this.mAnswerFormat = mAnswerFormat;
    }

    public String getmAnswerFormatName() {
        return mAnswerFormatName;
    }

    public void setmAnswerFormatName(String mAnswerFormatName) {
        this.mAnswerFormatName = mAnswerFormatName;
    }
}
