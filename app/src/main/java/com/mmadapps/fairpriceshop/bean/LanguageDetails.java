public class LanguageDetails {

    private String mId;
    private String mName;
    private String mNameL1;
    private String mCode;
    private String mStateLanguageId;
    private String mIsDefault;

    public String getmId() {
        return mId;
    }

    public void setmId(String mId) {
        this.mId = mId;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmNameL1() {
        return mNameL1;
    }

    public void setmNameL1(String mNameL1) {
        this.mNameL1 = mNameL1;
    }

    public String getmCode() {
        return mCode;
    }

    public void setmCode(String mCode) {
        this.mCode = mCode;
    }

    public String getmStateLanguageId() {
        return mStateLanguageId;
    }

    public void setmStateLanguageId(String mStateLanguageId) {
        this.mStateLanguageId = mStateLanguageId;
    }

    public String getmIsDefault() {
        return mIsDefault;
    }

    public void setmIsDefault(String mIsDefault) {
        this.mIsDefault = mIsDefault;
    }
}
